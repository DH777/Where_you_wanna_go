    /**
     * Represents an asynchronous login/registration task used to authenticate
     * the user.
     */
    public class UserLoginTask extends AsyncTask<Void, Void, Boolean> {

        private final String mEmail;
        private final String mPassword;

        UserLoginTask(String email, String password) {
            mEmail = email;
            mPassword = password;
        }

        @Override
        protected Boolean doInBackground(Void... params) {
            if (reg) {
                User u = new User(mEmail, mPassword, new HashSet<Location>());
                DatabaseReference r = usersRef.child(mEmail);
                r.child("name").setValue(mEmail);
                r.child("password").setValue(mPassword);
                ArrayList<Location> l = new ArrayList<Location>();
                l.add(new Location("A", "desc", -5, 10, false));

                r.child("locations").setValue(l);

                reg = false;
                return true;
            } else {
                boolean check = snap.child("users").child(mEmail).child("password").getValue().equals(mPassword);
                if (check) {
                    return true;
                }
                return false;
            }
            // TODO: register the new account here.
            //return true;
        }

        @Override
        protected void onPostExecute(final Boolean success) {
            mAuthTask = null;
            showProgress(false);

            if (success) {
                finish();
            } else {
                mEmailView.setError(getString(R.string.error_login_incorrect));
                mEmailView.requestFocus();
            }
        }

        @Override
        protected void onCancelled() {
            mAuthTask = null;
            showProgress(false);
        }
    }
